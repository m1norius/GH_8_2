package main;

public class Car {
	private int maxCount;
	private String model;
	private String type;
	private String color;
	private int maxSpeed;
	

	public Car(int maxCount, String model, String type, String color, int maxSpeed){
		this.maxCount = maxCount;
		this.model = model;
		this.type = type;
		this.color = color;
		this.maxSpeed = maxSpeed;
	}
	
}
